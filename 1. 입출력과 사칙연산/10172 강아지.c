// 강아지 출력
// \를 출력하려면 두 번을 써줘야 하므로 조심
// 줄 바꿈도 \n이므로 정확하게 세면서 해주기
// " 큰 따옴표를 출력하고 싶으면 \" 해야함!
// 마지막 줄은 \n 필요 없고 마지막에 리턴해주기

#include <stdio.h>

int main(void)
{
    printf("|\\_/|\n");
    printf("|q p|   /}\n");
    printf("( 0 )\"\"\"\\\n");
    printf("|\"^\"`    |\n");
    printf("||_/=\\\\__|");

    return 0;
}